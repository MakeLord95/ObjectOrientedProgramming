package Module_6_3.model;

public class Pet {
    private int x = 0;
    private int y = 0;

    public Pet() {
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
